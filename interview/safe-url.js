    window.getQueryFromURL = function(url) {
        url = url; 
        var tmp = url.split('?'),
            query = (tmp[1] || "").split('#')[0].split('&'),
            params = {};
        for (var i=0; i<query.length; i++) {
            var arg = query[i].split('=');
            params[arg[0]] = encodeURIComponent(arg[1].html(false).html(false).replace(/\s/g,"+"));
        }
        return params;
    };

